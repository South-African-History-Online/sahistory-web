// ---------------------------------------------------------------------------
// CLI entry point. No-op when this file is require()'d as a library.
// ---------------------------------------------------------------------------
if (PHP_SAPI === 'cli' && isset($argv[0]) && realpath($argv[0]) === realpath(__FILE__)) {
  $in = $argv[1] ?? '';
  if ($in === '' || in_array($in, ['-h', '--help'], TRUE)) {
    fwrite(STDERR, "SAHO Classroom deck renderer\n\n");
    fwrite(STDERR, "Usage: php render.php <schema.json> [out.html]\n");
    fwrite(STDERR, "       php render.php <schema.json> > out.html\n");
    exit($in === '' ? 1 : 0);
  }
  try {
    $html = saho_classroom_render_deck_file($in);
  }
  catch (\Throwable $err) {
    fwrite(STDERR, 'error: ' . $err->getMessage() . "\n");
    exit(1);
  }
  $out = $argv[2] ?? '';
  if ($out !== '') {
    if (file_put_contents($out, $html) === FALSE) {
      fwrite(STDERR, "error: failed to write {$out}\n");
      exit(1);
    }
    fwrite(STDERR, "wrote {$out}\n");
  }
  else {
    fwrite(STDOUT, $html);
  }
  exit(0);
}
